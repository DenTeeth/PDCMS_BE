package com.dental.clinic.management.domain.enums;

/**
 * Work shift types for fixed shifts.
 * Each clinic has 3 predefined shifts.
 */
public enum WorkShiftType {
    /**
     * Morning shift: 08:00 - 12:00.
     */
    MORNING,
    
    /**
     * Afternoon shift: 13:00 - 17:00.
     */
    AFTERNOON,
    
    /**
     * Evening shift: 18:00 - 21:00.
     */
    EVENING
}
