package com.dental.clinic.management.working_schedule.enums;

/**
 * Enum representing types of holidays.
 */
public enum HolidayType {
    /**
     * National holidays (e.g., Tet, National Day).
     * Applies to all employees nationwide.
     */
    NATIONAL,

    /**
     * Company-specific holidays (e.g., Company Foundation Day, Team Building).
     * Applies to company employees only.
     */
    COMPANY
}
